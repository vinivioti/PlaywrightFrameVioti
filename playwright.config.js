// Função para obter o timestamp no horário local do Brasil
function getLocalTimestamp() {
  const now = new Date();
  return now.toLocaleString('pt-BR', { 
    timeZone: 'America/Sao_Paulo', 
    year: 'numeric', 
    month: '2-digit', 
    day: '2-digit', 
    hour: '2-digit', 
    minute: '2-digit', 
    second: '2-digit' 
  }).replace(/\D/g, '-'); // Remove caracteres especiais
}

